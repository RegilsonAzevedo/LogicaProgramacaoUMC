//variaveis - var e let
// declarando var
var texto = 'Hello Word'
console.log(texto)

// declarando let
let nome = 'Regilson Azevedo'
console.log('Nome:',nome)

// atribuindo um novo valor
nome = 'Re Azevedo'
console.log('Nome:',nome)

// atribuindo novo valor - var
texto = 'Olá, Mundo'
console.log(texto)

// constantes - const (só pode dar valor uma única vez, não aceita nova atribuição de valor)
const valorPi = 3.14
console.log('O valor de Pi é igual a', valorPi)
