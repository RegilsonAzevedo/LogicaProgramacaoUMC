// Operadores Aritiméticos


let n1 = 4
let n2 = 2

// Operadores soma
let soma = n1 + n2
console.log('O valor da soma de n1 mais n2 é', soma)

// Operador subtração
let subtracao = n1 - n2
console.log('O valor da subtração de n1 mais n2 é', subtracao)

// Operador multiplicação
let mult = n1 *n2
console.log('O valor da multiplicação de n1 mais n2 é', mult)

// Operador divisão
let div = n1 / n2
console.log('O valor da divisão de n1 mais n2 é', div)

// Operador modulo
let modulo = n1 % n2
console.log('O restante da operação é',modulo)

// Operador de potencia
let potencia = Math.pow(n1,n2)
console.log('A resultado da potência é',potencia)

// operador raiz quadrada
let raiz_quadrada = Math.sqrt(n1)
console.log('A raiz quadrada é ', raiz_quadrada)